export const getCategories = () => {
    return new Promise (resolve => {
        setTimeout(() => {
            resolve(categories)
        },2000)
    })
}

//export nombrado
export const getProducts = (categoryId) => {
    return new Promise (resolve => {
        setTimeout (() => {
            resolve(categoryId ? products.filter(prod => prod.category === categoryId) : products)
        }, 2000)
    }) 
}

